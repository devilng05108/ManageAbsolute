    private void jbtnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnTotalActionPerformed
        int singleRoom = 175;
        int deluxeRoom = 250;
        int familyRoom = 350;
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        Date checkin_Date = null;
        Date checkout_Date = null;
        try {
            checkin_Date = format.parse(format.format(jDateChooser2.getDate()));
            checkout_Date = format.parse(format.format(jDateChooser3.getDate()));
        } catch (Exception e) {
            e.printStackTrace();
        }
        long CheckDiff = checkout_Date.getTime() - checkin_Date.getTime();
        int days = (int) (CheckDiff / 1000 / 60 / 60 / 24);
        if (jcbboxroomtype.getSelectedItem().equals("Single")) {
            jtxttotal.setText("RM" + Integer.toString(days * singleRoom));
        } else if (jcbboxroomtype.getSelectedItem().equals("Deluxe")) {
            jtxttotal.setText("RM" + Integer.toString(days * deluxeRoom));
        } else {
            jtxttotal.setText("RM" + Integer.toString(days * familyRoom));
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{jtxtcustomerref.getText(), jtxtfirstname.getText(), jtxtsurname.getText(), jtxtaddress1.getText(), jtxtaddress2.getText(), jtxtpostcode.getText(),
            jtxtmobileno.getText(), jtxtemail.getText(), jcbboxnationality.getSelectedItem(), jDateChooser1.getDate(), jcbboxidtype.getSelectedItem(),
            jcbboxgender.getSelectedItem(), jDateChooser2.getDate(), jDateChooser3.getDate(), jcbboxroomtype.getSelectedItem(), jcbboxroomno.getSelectedItem()});
    }//GEN-LAST:event_jbtnTotalActionPerformed

